function Multiplicacao() {
    let n1 = 10
    let n2 = 8
    let res = n1 * n2
    return res      /* 'return' retorna o resultdado de 'res' quando chamar a função */
}

let num = Multiplicacao()        /* variavel 'num' recebe o retorno da função 'Multiplicacao' */
console.log(num)     /* 'console.log' vai imprimir a variável 'num' */
